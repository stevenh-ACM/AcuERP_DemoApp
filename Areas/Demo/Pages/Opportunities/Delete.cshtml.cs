using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;

using AcuERP_DemoApp.Data;
using AcuERP_DemoApp.Entities;

namespace AcuERP_DemoApp.Areas.Demo.Pages.Opportunities;

public class DeleteModel : PageModel
{
    private readonly AppDbContext _context;

    public DeleteModel(AppDbContext context)
    {
        _context = context;
    }

    [BindProperty]
    public Opportunity Opportunity { get; set; }

    public async Task<IActionResult> OnGetAsync(int? id)
    {
        if (id == null)
        {
            return NotFound();
        }

        Opportunity = await _context.Opportunities.FirstOrDefaultAsync(m => m.Id == id);

        if (Opportunity == null)
        {
            return NotFound();
        }
        return Page();
    }

    public async Task<IActionResult> OnPostAsync(int? id)
    {
        if (id == null)
        {
            return NotFound();
        }

        Opportunity = await _context.Opportunities.FindAsync(id);

        if (Opportunity != null)
        {
            _context.Opportunities.Remove(Opportunity);
            await _context.SaveChangesAsync();
        }

        return RedirectToPage("./Index");
    }
}
